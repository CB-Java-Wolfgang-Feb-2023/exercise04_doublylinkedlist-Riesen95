
public class Node {
    private int value; // Wert des Knotens
    private Node next; // Verweis auf den n√§chsten Knoten
    private Node previous; // Verweis auf den vorherigen Knoten

    public Node(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    public Node getPrevious() {
        return previous;
    }

    public void setPrevious(Node previous) {
        this.previous = previous;
    }
}